import math

x = 5
x += 3
x -= 3
x *= 3
x /= 3

print(10 / 3)  # 3.3333
print(10 // 3)  # 3
print(10 % 3)  # 1
print(10**3)  # 10^3 = 1000

print("ceil(4.4) = ", math.ceil(4.4))  # 5
print("floor(4.4) = ", math.floor(4.4))  # 4
print("fabs(-4.4) = ", math.fabs(-4.4))  # 4.4
print("factorial(4) = ", math.factorial(4))  # Factorial = 1 * 2 * 3 * 4 = 24
print("fmod(5,4) = ", math.fmod(5, 4))  # 1.0 remainder of division
print("trunc(4.2) = ", math.trunc(4.2))  # 4 Receive a float and return an int
print("pow(2,2) = ", math.pow(2, 2))  # 4.0 x^y
print("sqrt(4) = ", math.sqrt(4))  # 2.0 square root
print("math.e = ", math.e)  # 2.718281828459045
print("math.pi = ", math.pi)  # 3.141592653589793
print("exp(4) = ", math.exp(4))  # e^x = 54.598150033144236
print("log(20) = ", math.log(20))  # 2.995732273553991
print("log(1000,10) = ", math.log(1000, 10))  # 2.9999999999999996
print("log10(1000) = ", math.log10(1000))  # 3.0
# sin, cos, tan, asin, acos, atan, atan2, asinh, acosh,
# atanh, sinh, cosh, tanh
print("sin(0) ", math.sin(0))
print("degrees(1.5708) = ", math.degrees(1.5708))  # rad to deg = 90.00021045914971
print("radians(90) = ", math.radians(90))  # deg to rad = 1.5707963267948966
